// Switch
// dice value : 1 - 6

let dice = 3;

switch (dice) {
  case 1:
    console.log("you got one");
    break;
  case 2:
    console.log("you got two");
    break;
  case 3:
    console.log("you got three");
    break;
    default:
      console.log('you did not roll the dice')
}

